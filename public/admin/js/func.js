$(document).on('click', '.edit', function(e) {
    e.preventDefault();
    var href = $(this).attr('href');
        jQuery.ajax({
          url: href,
          type: 'GET',
          dataType: 'json',
          data: '',
          
          success: function(data) {
            $('#editTitle').val(data.desc.categorychiTitle);
            $('#editSlug').val(data.desc.categorychiSlug);
            $('#editId').val(data.desc.categorychiId);
          }
        });
});

